#include "libft.h"

int	ft_isalnum(int c)
{
	int flag;

	flag = '1';
	if (!(c >= 97 && c <= 122))
	{
		if (!(c >= 65 && c <= 90))
		{
			if (!(c >= 48 && c <= 57))
			{
				return (0);
			}
		}
	}
	return (flag);
}
