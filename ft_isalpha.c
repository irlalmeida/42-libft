#include "libft.h"
int ft_isalpha(int c)
{
    int flag;

    flag =  '1';

    if (!(c >= 97 && c <= 122))
    {
        if (!(c >= 65 && c <= 90))
        {
            return (0);
        }
    }
    return (flag);
}
